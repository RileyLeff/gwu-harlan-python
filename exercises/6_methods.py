# methods are functions associated with a particular datatype. 
# as you might have guessed, the basic python datatypes come pre-loaded with plenty of useful methods.

# while you use functions like this:
#   my_function(my_object)

# you can call methods like this:
#   my_object.cool_method(potentially_arguments_go_here)

# here's a common one: lists have a method called append that lets you add more elements to the end of the list

a = [1,2,3,4,5]
a.append(95)

